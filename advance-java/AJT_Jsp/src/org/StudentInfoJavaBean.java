package org;

public class StudentInfoJavaBean {
		String en , name;
		int age;
		public String getEn() {
			return en;
		}
		public void setEn(String en) {
			this.en = en;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
}
